function test_generic(T=Float64)
	model = GenericModel{T}(Clarabel.Optimizer{T})
	@variable(model, -1 <= x[1:2] <= sqrt(T(2)))
	@constraint(model, c, x[1] == T(2) * x[2])
	@objective(model, Min, 3x[1]^2 + 2x[2]^2 - x[1] - T(4) * x[2])
		optimize!(model)
	solution_summary(model)
	return (;x=value.(x), objective= objective_value(model), type=typeof(objective_value(model)))
end

